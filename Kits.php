<?php


class Kits {

    private $chip;
    private $numero;
    private $remera;
    private $medalla;

    

    /**
     * Get the value of chip
     */ 
    public function getChip()
    {
        return $this->chip;
    }

    

    /**
     * Get the value of numero
     */ 
    public function getNumero()
    {
        return $this->numero;
    }

    /**
     * Get the value of remera
     */ 
    public function getRemera()
    {
        return $this->remera;
    }

    /**
     * Get the value of medalla
     */ 
    public function getMedalla()
    {
        return $this->medalla;
    }
}